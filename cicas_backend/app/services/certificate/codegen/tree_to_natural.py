# A faithful, DETERMINISTIC snake_case summary of a DSL tree, used to name
# generated lints `cicasgen_<slug>_<rule_id>` instead of the opaque
# `cicasgen_<rule_id>`. Mechanical (no LLM, no per-rule table): the verb comes
# from the atom class, the subject from its primary field/oid/bit attribute.

# Short, readable verb per atom class. Anything not listed falls back to a
# snake_cased class name, so new atoms still get a sensible (if longer) slug.
_SLUG_VERB: dict[str, str] = {
    "IsCA": "is_ca", "IsRootCA": "root_ca", "IsSubCA": "sub_ca", "IsServerCert": "server_cert",
    "IsSubscriberCert": "subscriber_cert", "IsEndEntity": "end_entity",
    "ExtPresent": "present", "ExtCritical": "critical", "ExtNotCritical": "not_critical",
    "ExtContentNonEmpty": "content_non_empty",
    "KeyUsageHas": "ku_has", "ExtKeyUsageHas": "eku_has",
    "FieldEq": "eq", "FieldNonEmpty": "present", "FieldEmpty": "absent",
    "FieldInSet": "in_set", "FieldNotInSet": "not_in_set",
    "FieldLenInRange": "len", "FieldNumericInRange": "value",
    "FieldEncodedAs": "encoded_as", "FieldCount": "count",
    "FieldMatchesRegex": "matches", "FieldNotMatchesRegex": "not_matches",
    "FieldContains": "contains",
    "OidListContains": "list_contains", "IPListAllOctetCount": "ip_octets",
    "RSAModulusBitsInRange": "rsa_modulus_bits", "RSAPublicExponentInRange": "rsa_exponent",
    "PublicKeyAlgorithmIs": "pubkey_alg", "DNEmpty": "dn_empty",
}
# Attribute that names the SUBJECT of the atom, in priority order.
_SLUG_KEY_ATTRS = ("oid", "bit", "field", "scalar_field", "list_field",
                   "algorithm", "holder", "value", "pattern", "date_field")
# Atoms whose subject reads better BEFORE the verb (e.g. "aia_not_critical",
# "serial_number_value") rather than after ("eku_has_server_auth").
_SLUG_SUBJECT_FIRST = {
    "ExtPresent", "ExtCritical", "ExtNotCritical",
    "FieldEq", "FieldNonEmpty", "FieldEmpty", "FieldInSet", "FieldNotInSet",
    "FieldLenInRange", "FieldNumericInRange", "FieldEncodedAs", "FieldCount",
    "FieldMatchesRegex", "FieldNotMatchesRegex", "FieldContains",
    "OidListContains", "IPListAllOctetCount",
}


def _slug_token(s) -> str:
    """Normalize one identifier to snake_case ascii: CamelCase/dotted -> snake,
    strip a trailing 'OID' (AiaOID -> aia), drop non-[a-z0-9]."""
    s = re.sub(r"(?<=[a-z0-9])(?=[A-Z])", "_", str(s))
    s = s.replace(".", "_").lower()
    s = re.sub(r"_?oid$", "", s)
    s = re.sub(r"[^a-z0-9]+", "_", s).strip("_")
    return s


def _atom_slug(n) -> str:
    cn = type(n).__name__
    verb = _SLUG_VERB.get(cn) or _slug_token(cn)
    key = None
    for a in _SLUG_KEY_ATTRS:
        v = getattr(n, a, None)
        if isinstance(v, (list, tuple)):
            v = v[0] if v else None
        if v not in (None, ""):
            key = _slug_token(v)
            if key:
                break
    if not key:
        return verb
    return f"{key}_{verb}" if cn in _SLUG_SUBJECT_FIRST else f"{verb}_{key}"


def _walk_slug(n, depth: int = 0) -> str:
    cn = type(n).__name__
    if cn == "Not":
        return "not_" + _walk_slug(n.inner, depth + 1)
    if cn == "When":
        return f"when_{_walk_slug(n.cond, depth + 1)}_{_walk_slug(n.main, depth + 1)}"
    if cn in ("And", "Or"):
        sep = "_and_" if cn == "And" else "_or_"
        kids = [k for k in (_walk_slug(p, depth + 1) for p in n.parts) if k]
        if depth > 0 and len(kids) > 2:        # cap nested fan-out
            kids = kids[:2]
        return sep.join(kids) if kids else _slug_token(cn)
    return _atom_slug(n)


def tree_slug(tree, maxlen: int = 56) -> str:
    """Compact, faithful, deterministic snake_case slug describing a DSL tree,
    for zlint-style generated lint names. Returns 'rule' if the tree can't be
    walked (never raises). Truncates on a word boundary."""
    try:
        s = _walk_slug(tree)
    except Exception:
        s = ""
    s = re.sub(r"_+", "_", s or "").strip("_")
    if len(s) > maxlen:
        s = s[:maxlen].rsplit("_", 1)[0].strip("_") or s[:maxlen].strip("_")
    return s or "rule"


