def _sanitize_pkg_name(rule_id, slug: str = "") -> str:
    # Distinct, unmistakable namespace so generated lints are recognizable at a
    # glance and can NEVER be mistaken for / counted as zlint-authored lints.
    # zlint names use e_/w_/n_ severity prefixes; "cicasgen_" deliberately does
    # not, and the coverage matcher + zlint extractor both reject this prefix.
    # When a descriptive slug is available (derived from the DSL tree), the name
    # mimics zlint's readable form — cicasgen_<slug>_<rule_id> — instead of the
    # opaque cicasgen_<rule_id>. The trailing rule_id guarantees uniqueness (the
    # package name doubles as the on-disk directory) without a global registry.
    if slug:
        name = f"cicasgen_{slug}_{int(rule_id)}"
    else:
        name = f"cicasgen_{int(rule_id):04d}"
    name = re.sub(r"[^a-z0-9_]", "_", name.lower())
    return re.sub(r"_+", "_", name).strip("_")


def _struct_name(rule_id) -> str:
    return f"CicasGen{int(rule_id):04d}"


def _tree_slug(tree) -> str:
    """Descriptive snake_case slug for a DSL predicate tree (faithful, no LLM).
    Empty string when no tree is given or the walk fails — callers then fall
    back to the opaque rule_id-only name."""
    if tree is None:
        return ""
    try:
        from .tree_to_natural import tree_slug
        return tree_slug(tree)
    except Exception:
        return ""


def _go_string(s: str) -> str:
    out = '"'
    for ch in s or "":
        if ch == "\\": out += "\\\\"
        elif ch == '"': out += '\\"'
        elif ch == "\n": out += "\\n"
        elif ch == "\r": out += "\\r"
        elif ch == "\t": out += "\\t"
        elif ord(ch) < 0x20 or ord(ch) > 0x7E:
            out += f"\\u{ord(ch):04x}"
        else:
            out += ch
    out += '"'
    return out


def _source_const(source: str) -> str:
    s = (source or "").upper()
    if "RFC" in s or "5280" in s: return "RFC5280"
    if "CABF" in s or "BR" in s:  return "CABFBaselineRequirements"
    return "Community"


def _build_imports(imps: set) -> str:
    """Sorted, with stdlib block separated from external."""
    stdlib = sorted(i for i in imps if "/" not in i or i.startswith(("math", "regexp", "time", "fmt")))
    external = sorted(i for i in imps if i not in stdlib)
    lines = []
    for i in stdlib:
        lines.append(f'\t"{i}"')
    if external and stdlib:
        lines.append("")
    for i in external:
        lines.append(f'\t"{i}"')
    return "\n".join(lines)


@dataclass
class RenderedLint:
    rule_id: int
    pkg: str
    struct_name: str
    file_content: str
    imports: set
    statement: str
    template_id: str
    slots: dict
    severity: str = "lint.Error"


def render_full_lint_from_tree(rule_id: int,
                               source: str,
                               section: str,
                               rule_text: str,
                               execute_body: str,
                               imports: set,
                               severity: str = "lint.Error",
                               label: str = "",
                               tree=None) -> RenderedLint:
    """Tree-codegen variant of render_full_lint: takes a pre-rendered
    execute_body + imports (from tree_codegen.render_from_tree) and wraps
    them in the standard zlint file shell. No catalog template required.

    `tree` (the DSL predicate) is used only to derive a descriptive lint name
    (cicasgen_<slug>_<rule_id>); pass it when available."""
    pkg = _sanitize_pkg_name(rule_id, _tree_slug(tree))
    struct = _struct_name(rule_id)
    file_content = _FILE_TEMPLATE.format(
        pkg=pkg,
        imports=_build_imports(imports),
        struct_name=struct,
        name_lit=_go_string(pkg),
        desc_lit=_go_string(rule_text or ""),
        citation_lit=_go_string(f"{source}: {section}" if section else (source or "")),
        source_const=_source_const(source),
        execute_body=execute_body,
    )
    return RenderedLint(
        rule_id=int(rule_id), pkg=pkg, struct_name=struct,
        file_content=file_content, imports=imports,
        statement=label or "", template_id="<tree>", slots={},
        severity=severity,
    )


