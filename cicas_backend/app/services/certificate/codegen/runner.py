def render_full_lint_multi_branch(rule_id: int,
                                  source: str,
                                  section: str,
                                  rule_text: str,
                                  branches: list,
                                  imports: set,
                                  tree=None) -> RenderedLint:
    """Emit ONE Go file registering N lints from `branches`, each with
    its own severity, struct, and Execute body. Used for rules that
    bundle clauses at different severities (e.g. MUST NOT + NOT
    RECOMMENDED in one source rule).

    branches: list of dicts with keys:
        - label:        str (used as suffix for the registered Lint name)
        - severity:     "lint.Error" | "lint.Warn" | "lint.Notice"
        - execute_body: pre-rendered Go body (from tree_codegen)
        - predicate:    (optional) DSL tree, used to derive the lint name

    Returns a RenderedLint whose file_content has multiple
    RegisterCertificateLint calls in init(), each pointing at a
    distinct branch struct. The `severity` field on the returned
    RenderedLint is the HIGHEST severity among branches (for routing
    purposes).
    """
    if not branches:
        raise ValueError("render_full_lint_multi_branch: empty branches")
    slug = _tree_slug(tree) or _tree_slug(branches[0].get("predicate"))
    pkg = _sanitize_pkg_name(rule_id, slug)
    base_struct = _struct_name(rule_id)

    # Build per-branch artifacts
    regs: list = []
    branch_blocks: list = []
    for i, br in enumerate(branches):
        suffix_label = (br.get("label") or "").strip() or f"b{i}"
        # Lint name + struct name must be Go-unique within the package
        name_suffix = f"{suffix_label}"
        # sanitize: lowercase, only [a-z0-9_]
        import re
        name_suffix = re.sub(r"[^a-z0-9_]", "_", name_suffix.lower())[:40]
        if not name_suffix:
            name_suffix = f"b{i}"
        lint_name = f"{pkg}_{name_suffix}"
        struct_n = f"{base_struct}{i}_{name_suffix}"
        regs.append(_FILE_TEMPLATE_MULTI_REGISTRATION.format(
            name_lit=_go_string(lint_name),
            desc_lit=_go_string(rule_text or ""),
            citation_lit=_go_string(f"{source}: {section}" if section else (source or "")),
            source_const=_source_const(source),
            struct_name=struct_n,
        ))
        branch_blocks.append(_FILE_TEMPLATE_MULTI_BRANCH.format(
            struct_name=struct_n,
            execute_body=br["execute_body"],
        ))

    registrations = "\n".join(regs)
    body_blocks = "\n".join(branch_blocks)
    header = _FILE_TEMPLATE_MULTI_HEADER.format(
        pkg=pkg, imports=_build_imports(imports),
        registrations=registrations,
    )
    file_content = header + body_blocks

    sev_rank = {"lint.Error": 3, "lint.Warn": 2, "lint.Notice": 1}
    top_sev = max((br["severity"] for br in branches),
                  key=lambda s: sev_rank.get(s, 0))

    return RenderedLint(
        rule_id=int(rule_id), pkg=pkg, struct_name=base_struct,
        file_content=file_content, imports=imports,
        statement=" | ".join(b.get("label","") for b in branches),
        template_id="<tree_multi>", slots={},
        severity=top_sev,
    )


def render_full_lint(rule_id: int,
                     source: str,
                     section: str,
                     rule_text: str,
                     template_id: str,
                     slots: dict,
                     requirement_level: str = "") -> RenderedLint:
    if template_id not in catalog.CATALOG_BY_ID:
        raise dsl.DSLError(f"unknown template_id '{template_id}'")
    t = catalog.CATALOG_BY_ID[template_id]
    errs = catalog.fill_check(t, slots)
    if errs:
        raise dsl.DSLError(f"slot fill_check failed: {errs}")
    severity = catalog.severity_for(rule_text=rule_text,
                                    requirement_level=requirement_level)
    out = catalog.render_body(t, slots, severity=severity)
    body = out["execute_body"]
    imps = out["imports"]
    statement = catalog.render_statement(t, slots)

    # template_id is already a readable handle (e.g. "T_ext_non_critical");
    # use it (minus the "T_" prefix) as the descriptive name slug.
    tmpl_slug = re.sub(r"^t_", "", str(template_id).lower())
    pkg = _sanitize_pkg_name(rule_id, tmpl_slug)
    struct = _struct_name(rule_id)
    file_content = _FILE_TEMPLATE.format(
        pkg=pkg,
        imports=_build_imports(imps),
        struct_name=struct,
        name_lit=_go_string(pkg),
        desc_lit=_go_string(rule_text or ""),
        citation_lit=_go_string(f"{source}: {section}" if section else (source or "")),
        source_const=_source_const(source),
        execute_body=body,
    )
    return RenderedLint(
        rule_id=int(rule_id), pkg=pkg, struct_name=struct,
        file_content=file_content, imports=imps,
        statement=statement, template_id=template_id, slots=slots,
        severity=severity,
    )


