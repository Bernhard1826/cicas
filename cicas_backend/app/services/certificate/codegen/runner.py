GO_MOD = """\
module {mod}

go 1.21

replace github.com/zmap/zlint/v3 => {zlint}
"""


def build_workspace(workspace: Path) -> None:
    """Lay out a scratch Go module that depends on local zlint + zcrypto."""
    (workspace / "pkgs").mkdir(parents=True, exist_ok=True)
    (workspace / "go.mod").write_text(GO_MOD.format(
        mod=MODNAME, zlint=ZLINT_PATH))
    # tiny main so go-tooling doesn't complain
    (workspace / "main.go").write_text(
        "package main\n\nfunc main() {}\n")
    # populate go.sum + transitive deps
    for pkg in ("github.com/zmap/zlint/v3", "github.com/zmap/zcrypto"):
        r = subprocess.run(["go", "get", pkg], cwd=workspace,
                           capture_output=True, timeout=300, text=True)
        if r.returncode != 0:
            raise RuntimeError(f"go get {pkg} failed: {r.stderr[:500]}")


def write_lint(workspace: Path, rl: RenderedLint) -> Path:
    pkg_dir = workspace / "pkgs" / rl.pkg
    pkg_dir.mkdir(parents=True, exist_ok=True)
    out_file = pkg_dir / f"{rl.pkg}.go"
    out_file.write_text(rl.file_content)
    return out_file


def go_build(workspace: Path, rl: RenderedLint, timeout: int = 60) -> tuple:
    """Run `go build ./pkgs/<pkg>` in workspace.
    Returns (ok: bool, stderr: str)."""
    pkg_path = f"./pkgs/{rl.pkg}"
    env = os.environ.copy()
    env.setdefault("GO111MODULE", "on")
    env.setdefault("GOFLAGS", "-mod=mod")
    try:
        proc = subprocess.run(
            ["go", "build", pkg_path],
            cwd=workspace, env=env,
            stdout=subprocess.PIPE, stderr=subprocess.PIPE,
            timeout=timeout,
        )
    except subprocess.TimeoutExpired:
        return False, "timeout"
    except FileNotFoundError:
        return False, "go binary not found"
    if proc.returncode == 0:
        return True, ""
    stderr = (proc.stderr or b"").decode("utf-8", errors="replace")
    stdout = (proc.stdout or b"").decode("utf-8", errors="replace")
    return False, (stderr or stdout)[:1500]


def compile_check_one(rl: RenderedLint, workspace: Optional[Path] = None) -> tuple:
    """If workspace is None, create a fresh tempdir. Returns (ok, stderr)."""
    if workspace is None:
        with tempfile.TemporaryDirectory(prefix="cicas_v2_") as tmp:
            ws = Path(tmp)
            build_workspace(ws)
            write_lint(ws, rl)
            return go_build(ws, rl)
    write_lint(workspace, rl)
    return go_build(workspace, rl)


